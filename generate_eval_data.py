def generate_test_data():
    """
    生成测试数据并返回结构化列表，自动检测重复

    Returns:
        list: 包含200组测试数据的列表
    """
    result = []
    seen_enterprise = set()
    seen_standard = set()
    dup_count = 0

    for item in TEST_DATA:
        group_id = item[0]
        enterprise_address = item[1]
        standard_address = item[2]
        consistency_type = item[3]
        expected_match = item[4]
        semantic_diff = item[5]

        enterprise_id = f"EVAL_E{group_id:04d}"
        enterprise_name = f"测评企业{group_id:04d}"
        standard_addr_code = f"EVAL_A{group_id:04d}"
        house_code = f"HC{group_id:08d}"

        if enterprise_address in seen_enterprise:
            print(f"警告: 企业地址重复 - 组号{group_id}: {enterprise_address}")
            dup_count += 1
        if standard_address in seen_standard:
            print(f"警告: 标准地址重复 - 组号{group_id}: {standard_address}")
            dup_count += 1

        seen_enterprise.add(enterprise_address)
        seen_standard.add(standard_address)

        result.append({
            'group_id': group_id,
            'enterprise_id': enterprise_id,
            'enterprise_name': enterprise_name,
            'enterprise_address': enterprise_address,
            'standard_addr_code': standard_addr_code,
            'standard_address': standard_address,
            'house_code': house_code,
            'consistency_type': consistency_type,
            'expected_match': expected_match,
            'semantic_diff': semantic_diff,
        })

    print(f"企业地址去重检查: {len(seen_enterprise)}/{len(TEST_DATA)} 条唯一")
    print(f"标准地址去重检查: {len(seen_standard)}/{len(TEST_DATA)} 条唯一")
    if dup_count > 0:
        print(f"!!! 发现 {dup_count} 处重复，请修正 !!!")
    else:
        print("[OK] 无重复数据")
    return result


def save_to_csv(data, filepath):
    """
    将测试数据保存为CSV文件

    Args:
        data: 测试数据列表
        filepath: CSV文件保存路径
    """
    if not data:
        return

    fieldnames = [
        'group_id', 'enterprise_id', 'enterprise_name', 'enterprise_address',
        'standard_addr_code', 'standard_address', 'house_code',
        'consistency_type', 'expected_match', 'semantic_diff'
    ]

    with open(filepath, 'w', newline='', encoding='utf-8-sig') as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(data)

    print(f"CSV文件已保存: {filepath}")


def insert_to_database(data):
    """
    将测试数据插入数据库（不清空原有数据）

    Args:
        data: 测试数据列表

    Returns:
        tuple: (enterprise插入数, standard_address插入数)
    """
    conn = psycopg2.connect(
        dbname='postgres',
        user='postgres',
        password='123456',
        host='localhost',
        port=5432
    )
    conn.autocommit = False
    cur = conn.cursor()

    try:
        enterprise_count = 0
        standard_count = 0

        for item in data:
            cur.execute(
                "INSERT INTO enterprise (id, name, address) VALUES (%s, %s, %s)",
                (item['enterprise_id'], item['enterprise_name'], item['enterprise_address'])
            )
            enterprise_count += 1

            cur.execute(
                "INSERT INTO standard_address (addr_code, standard_addr, house_code) VALUES (%s, %s, %s)",
                (item['standard_addr_code'], item['standard_address'], item['house_code'])
            )
            standard_count += 1

        conn.commit()
        print(f"数据插入完成: enterprise表 {enterprise_count} 条, standard_address表 {standard_count} 条")
        return enterprise_count, standard_count

    except Exception as e:
        conn.rollback()
        print(f"数据插入失败: {e}")
        raise
    finally:
        cur.close()
        conn.close()


